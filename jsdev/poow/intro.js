//POO : 
// une application en programmation orientee objet se
// compose de plusieurs OBJETS  qui communiquent entre eux 
// par exemple , dans une application de gestion de stock , 
// on developpe des objets comme : produit , facture, commande ,client,...
// un objet : est une unite logiciel qui se compose de :
// 1- attributs (donnees) : caracteristiques de l'objet 
// exemple : pour l'objet produit : libelle,prix,qteStock
// 2- les methodes (programmes) : les fonctions qui traitent souvent des donnees
// exemple : afficherFicheProduit, ajouterProduit, supprimerProduit,
// ces attributs et methodes sont orientee produit
// RESUME : LA POO EST UN PARADIGME (METHODOLOGIE ) QUI CONSISTE 
// A DEVELOPPER L'APPLICATION OBJET PAR OBJET 



